package fundamentos;

/**
 * METODO MAIN Esse metodo e a porta de entrada de um programa em JAVA.
 * 
 * @author 'args' parametros passados por linha de comando.
 *
 */
public class Comentarios {
	public static void main(String[] args) {
		/*
		 * COMENTARIO DE MULTIPLAS LINHAS Imprime no console a palavra 'Primeiro' e em
		 * seguida cria uma nova linha. LEMBRANDO QUE O 'printLN' e a quebra da linha,
		 * ou seja, caso fosse so o 'print' seria impresso na mesma linha.
		 */
		System.out.println("Primeiro"); // SEMPRE TERMINAR COM O SINAL ';'
		// Cuidado com os comentarios obvios
		System.out.println("Programa");
	}

}
