package estruturacontrole;

public class For {
	public static void main(String[] args) {
		for (int cont = 0; cont <= 10; cont++) {
			System.out.println(cont);
			/* FOR CONTANDO DE 1 A 10
			 * A expressao FOR é muito parecida com o WHILE. No sentido de conter um
			 * contador inicial ou a definição da variável, uma expressão, e um incremento
			 * TODOS ELES SEPARADOS POR ';' OU SEJA, O WHILE é uma estrutura de repetição
			 * para uma quantidade INDETERMINADA enquanto o FOR é uma estrutura de repetição
			 * para uma quantidade DETERMINADA
			 */
		}
	}
}
